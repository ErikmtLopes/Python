#7. FUA para calcular a área de um triângulo, exibindo o resultado. A base e a altura são
# dados que devem ser lidos como entrada.

base = input('Para calcular a area do triangulo digite a sua base: ')
alt = input('Agora digite a altura: ')

area = float(float(base) * float(alt))/2

print('A area do triangulo é de',area)